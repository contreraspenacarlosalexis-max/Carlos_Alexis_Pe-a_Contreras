# Lista de números
numeros = [5, 12, 7, 20, 3]

# Usando funciones ya establecidas
print("Cantidad de números:", len(numeros))   # len() cuenta elementos
print("Número mayor:", max(numeros))          # max() obtiene el mayor
print("Número menor:", min(numeros))          # min() obtiene el menor
print("Suma total:", sum(numeros))            # sum() suma todos

# Pedir un número y convertirlo
dato = input("Ingresa un número: ")
numero = int(dato)   # int() convierte texto a entero

# Valor absoluto
print("Valor absoluto:", abs(numero))  # abs() elimina el signo